# Create an empty set
my_set = set()

# Adding elements
my_set.add(12)
my_set.add(26)
my_set.add(35)

# Printing the original set
print("Original set:", my_set)

# Modifying an element 
my_set.remove(26)
my_set.add(49)

# Printing the set after modification
print("Set after modification:", my_set)

# Removing an element
my_set.remove(35)

# Printing the set after removal
print("Set after removal:", my_set)