# Create an empty dictionary
my_dict = {}

# Adding elements
my_dict['apple'] = 10
my_dict['banana'] = 5
my_dict['orange'] = 8

# Printing the original dictionary
print("Original dictionary:", my_dict)

# Modifying an element
my_dict['banana'] = 7

# Printing the dictionary after modification
print("Dictionary after modification:", my_dict)

# Removing an element
del my_dict['orange']

# Printing the dictionary after deletion
print("Dictionary after deletion:", my_dict)